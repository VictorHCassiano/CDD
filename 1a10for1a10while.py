for x in range(1, 11):
    print(x, end=" ")

print("//For 1 a 10")
for y in range(10, 0, -1):
    print(y, end=" ")

contador = 1
print("//For 10 a 1")
while contador <= 10:
    print(contador, end=" ")
    contador += 1

print("//While 1 a 10")
contador = 10
while contador > 0:
    print(contador, end=" ")
    contador -= 1
print("//While 10 a 1")
